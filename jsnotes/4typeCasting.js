// Data typecasting

// String to Int
// we can convert string number to a number using
//1. parseInt()
//2. Number()
//3. Plus sign(+)

let num = '10'

let integerNum = parseInt(num)
console.log(integerNum)


integerNum = Number(num)
console.log(integerNum)


integerNum = +num
console.log(integerNum)

// String to Float
// we can convert string float number to a float number using
//1. parseFloat()
//2. Number()
//3. Plus sign(+)

num = '9.99'

let floatNum = parseFloat(num)
console.log(floatNum)


floatNum = Number(num)
console.log(floatNum)


floatNum = +num
console.log(floatNum)


// Float to Int

num = 4.44
let numInt = parseInt(num)
console.log(numInt) //4


